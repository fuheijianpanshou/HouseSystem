package ObjectClass;

public class RoomThing {
	public String roomNo;
	public String thingNo;
	public String thingName;
	public int thingAmount;
	public int thingPrice;
	public String getRoomNo() {
		return roomNo;
	}
	public void setRoomNo(String roomNo) {
		this.roomNo = roomNo;
	}
	public String getThingNo() {
		return thingNo;
	}
	public void setThingNo(String thingNo) {
		this.thingNo = thingNo;
	}
	public String getThingName() {
		return thingName;
	}
	public void setThingName(String thingName) {
		this.thingName = thingName;
	}
	public int getThingAmount() {
		return thingAmount;
	}
	public void setThingAmount(int thingAmount) {
		this.thingAmount = thingAmount;
	}
	public int getThingPrice() {
		return thingPrice;
	}
	public void setThingPrice(int thingPrice) {
		this.thingPrice = thingPrice;
	}
	

}
