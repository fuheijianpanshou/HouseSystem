package ObjectClass;

public class OutedRoomThing {
	private String CuNo;
	private String RoomNo;
	private String ThingNo;
	private String ThingName;
	private int ThingPrice;
	private int ThingOutAmount;
	private int PayMoney;
	
	public String getThingName() {
		return ThingName;
	}
	public void setThingName(String thingName) {
		ThingName = thingName;
	}
	public String getCuNo() {
		return CuNo;
	}
	public void setCuNo(String cuNo) {
		CuNo = cuNo;
	}
	public String getRoomNo() {
		return RoomNo;
	}
	public void setRoomNo(String roomNo) {
		RoomNo = roomNo;
	}
	public String getThingNo() {
		return ThingNo;
	}
	public void setThingNo(String thingNo) {
		ThingNo = thingNo;
	}
	public int getThingPrice() {
		return ThingPrice;
	}
	public void setThingPrice(int thingPrice) {
		ThingPrice = thingPrice;
	}
	public int getThingOutAmount() {
		return ThingOutAmount;
	}
	public void setThingOutAmount(int thingOutAmount) {
		ThingOutAmount = thingOutAmount;
	}
	public int getPayMoney() {
		return PayMoney;
	}
	public void setPayMoney(int payMoney) {
		PayMoney = payMoney;
	}
	

}
