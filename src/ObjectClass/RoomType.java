package ObjectClass;

public class RoomType {

	private String TypeNo;
	private String TypeName;
	private int PeopleNu;
	private int TypeArea;
	private int TypePrice;
	public String getTypeNo() {
		return TypeNo;
	}
	public void setTypeNo(String typeNo) {
		TypeNo = typeNo;
	}
	public String getTypeName() {
		return TypeName;
	}
	public void setTypeName(String typeName) {
		TypeName = typeName;
	}
	public int getPeopleNu() {
		return PeopleNu;
	}
	public void setPeopleNu(int peopleNo) {
		PeopleNu = peopleNo;
	}
	public int getTypeArea() {
		return TypeArea;
	}
	public void setTypeArea(int typeArea) {
		TypeArea = typeArea;
	}
	public int getTypePrice() {
		return TypePrice;
	}
	public void setTypePrice(int typePrice) {
		TypePrice = typePrice;
	}
	
}
