package ObjectClass;

public class HouseNews {
	private String newsNo;
	private String newsShowTime;
	private String newsKinds;
	private String newsContent;
	public String getNewsNo() {
		return newsNo;
	}
	public void setNewsNo(String newsNo) {
		this.newsNo = newsNo;
	}
	public String getNewsShowTime() {
		return newsShowTime;
	}
	public void setNewsShowTime(String newsShowTine) {
		this.newsShowTime = newsShowTine;
	}
	public String getNewsKinds() {
		return newsKinds;
	}
	public void setNewsKinds(String newsKinds) {
		this.newsKinds = newsKinds;
	}
	public String getNewsContent() {
		return newsContent;
	}
	public void setNewsContent(String newsContent) {
		this.newsContent = newsContent;
	}
	
}
